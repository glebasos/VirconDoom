void main()
{
    // --- group 1: signed 32-bit wraparound (BAM angle arithmetic depends on it)
    int maxInt = 0x7FFFFFFF;
    int wrap = maxInt + 1;                       // #1: wraps to INT_MIN
    Check( wrap < 0 );
    int minInt = wrap;

    int a = 0x70000000;
    int b = 0x20000000;
    int c = a + b;                               // #2: 0x90000000 (negative)
    Check( c < 0 );
    int d = c - b;                               // #3: subtract wraps back
    Check( d == a );

    // #4: IMUL wraps mod 2^32: 0x10000 * 0x10000 == 0
    int m = 0x10000 * 0x10000;
    Check( m == 0 );

    // #5: hex literal 0x80000000 (== ANG180)
    int ang180 = 0x80000000;
    Check( ang180 == minInt );

    // --- group 2: shifts
    // #6: >> is LOGICAL (zero-fill): (-1) >> 1 == 0x7FFFFFFF
    int neg1 = -1;
    Check( ( neg1 >> 1 ) == maxInt );
    // #7: >> by 16 of 0x80000000 gives 0x8000
    Check( ( ang180 >> 16 ) == 0x8000 );
    // #8: << out the top wraps: 3 << 31 == 0x80000000
    Check( ( 3 << 31 ) == ang180 );
    // #9: angle-to-fineangle idiom: (angle >> 19) & 8191 stays in range
    int fine = ( c >> 19 ) & 8191;
    Check( fine >= 0 && fine < 8192 );

    // --- group 3: division / modulo with negatives (truncation direction)
    // #10: -7 / 2 == -3 (truncate toward zero, like C99)
    Check( ( -7 / 2 ) == -3 );
    // #11: -7 % 8 == -7
    Check( ( -7 % 8 ) == -7 );

    // --- group 4: embedded ROM data readback
    Check( ProbeData[0] == 0 );                  // #12
    Check( ProbeData[2] == -1 );                 // #13
    Check( ProbeData[3] == maxInt );             // #14
    Check( ProbeData[4] == minInt );             // #15
    Check( ProbeData[5] == 0x12345678 );         // #16
    Check( ProbeData[11] == 0x00010000 );        // #17
    Check( ProbeData[15] == -7 );                // #18

    // --- group 5: function pointers stored as void* and called via typed ptr
    state_probe_t st;
    st.tics = 5;
    st.nextstate = 1;
    functype tmp = &ActionA;                     // &function -> typed fn ptr
    st.action = (void*)tmp;                      // typed fn ptr -> void* storage
    functype fp = (functype)st.action;           // #19: void* -> fn ptr round trip
    fp( 41 );
    Check( g_actionResult == 42 );
    tmp = &ActionB;
    st.action = (void*)tmp;
    fp = (functype)st.action;
    fp( 21 );
    Check( g_actionResult == 42 );               // #20

    // --- group 6: big initializer table integrity (spot checks)
    Check( BigTable[0].sprite == 0 );            // #21
    Check( BigTable[199].flags == 200 );         // #22
    Check( BigTable[123].sprite == 123 );        // #23
    Check( BigTable[57].frame == 7 );            // #24

    // --- group 7: struct copy by assignment
    bigstate_t sc = BigTable[42];
    Check( sc.sprite == 42 && sc.radius == 16 ); // #25

    // --- verdict loop
    while( true )
    {
        if( AllPassed )
        {
            clear_screen( color_green );
            print_at( 250, 160, "PROBES ALL GREEN" );
            ShowInt( 250, 180, checkNum );
        }
        else
        {
            clear_screen( color_red );
            print_at( 230, 150, "PROBE FAIL - FIRST FAIL CHECK #" );
            ShowInt( 250, 170, firstFail );
            print_at( 230, 190, "TOTAL CHECKS" );
            ShowInt( 340, 190, checkNum );
        }
        end_frame();
    }
}
